char	*ft_strcapitalize(char *str)
{
	while (*str != '\0')
	{
		if (*str != ' ')
		{
			*str++;
		}
		else if (*str >= 'a' && *str <= 'z')
		{
			*str = *str + 32;
		}
		*str++;
	}
	return (str);
}
