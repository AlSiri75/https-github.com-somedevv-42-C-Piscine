int		ft_str_is_lowercase(char *str)
{
	int a;
	int i;

	i = 0;
	a = 0;
	while (*str != '\0')
	{
		if (*str >= 'a' && *str <= 'z' && *str != '\0')
		{
			i++;
		}
		else
		{
			a++;
		}
		*str++;
	}
	if (a > 0)
	{
		return (0);
	}
	else
		return (1);
}
